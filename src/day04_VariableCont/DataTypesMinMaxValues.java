package day04_VariableCont;

public class DataTypesMinMaxValues {

    public static void main(String[] args) {
        byte bytemMin = Byte.MIN_VALUE;
        System.out.println("bytemMin = " + bytemMin);
        byte bbyteMax = Byte.MAX_VALUE;
        System.out.println("bbyteMax = " + bbyteMax);
        short shortMin = Short.MIN_VALUE;
        short shortMax = Short.MAX_VALUE;
        System.out.println("shortMax = " + shortMax);
        System.out.println("shortMax = " + shortMin);


    }
}
